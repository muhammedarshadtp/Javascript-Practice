
//  if/else statement

let x = 5

if(x>10){
    console.log("1")
}else if(x<5){
    console.log("2")
}else{
    console.log("5")
}


// Ternary Operator

let y=10

let z = y>10 ? "1":"0"

console.log(z)

// Switch statement


let a=10

switch (a) {
    case 1:
        console.log("1")
        break;
    case 10:
        console.log("10")
        break;
    default:
        console.log("0")
        break;
}